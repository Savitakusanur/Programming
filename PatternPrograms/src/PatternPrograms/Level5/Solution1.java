package PatternPrograms.Level5;

public class Solution1 {
public static void main(String[] args) {
	for(int i=1;i<=3;i++)
	{
		if(i==2)
		{
			System.out.print("*   *");
			System.out.println();
			continue;
		}
		for(int j=1;j<=3;j++)
		{
			
			System.out.print("* ");
		}
		System.out.println();
	}
}
}
